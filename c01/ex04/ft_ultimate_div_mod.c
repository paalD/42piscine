void	ft_ultimate_div_mod(int *a, int *b)
{
	int tmp_a;
	int tmp_b;

	if (*b != 0)
	{
		tmp_a = *a / *b;
		tmp_b = *a % *b;
		*a = tmp_a;
		*b = tmp_b;
	}
}
